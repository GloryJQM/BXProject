#import "PayMethodModel.h"

@implementation PayMethodModel

- (instancetype)initWithDictionary:(NSDictionary* )dictionary {
    self = [super init];
    if (self) {
        self.name = dictionary[@"name"];
        self.icon = dictionary[@"icon"];
        self.value = [dictionary[@"value"] integerValue];
    }
    return self;
}
@end
